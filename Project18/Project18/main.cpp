#include "game.h" // Подключаем заголовочный файл с объявлениями функций игры
#include <iostream> // Для ввода/вывода

using namespace std; // Используем пространство имен std для удобства


int main() {
    // Игровой цикл, который позволяет играть несколько раз
    char playAgain = 'y'; // Переменная для хранения ответа пользователя о повторной игре
    while (playAgain == 'y' || playAgain == 'Y') {
        // Вызов функции для начала игры
        startGame(); // Функция, определенная в game.h/game.cpp

        // Спрашиваем пользователя, хочет ли он сыграть снова
        cout << "Хотите сыграть снова? (y/n): "; // Выводим вопрос пользователю
        cin >> playAgain; // Считываем ответ пользователя
    }

    cout << "Спасибо за игру!" << endl; // Сообщение при выходе
    return 0; // Успешное завершение программы
}